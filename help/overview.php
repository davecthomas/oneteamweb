<div class="helpboxtext">
<span class="bold">1 Team Web</span> helps you manage your team-oriented memberships including sports teams, clubs, groups, and paid memberships. It takes care of the administrative side, so you can focus on what matters most: <span style="font-style: italic;">your team</span>.
<br/>
<br/>
<span class="bold">Features</span>
<ul>
<li>Secure logins for administrators that offer full team management capabilities</li>
<li>Secure logins for team leaders (coaches) that offer limited management capabilities (for example, tracking member attendance)</li>
<li>Secure logins for members to check personal attendance, payment schedule, progress through levels, and personal settings</li>
<li>Full attendance and enrollment tracking per program, per time period, or per student</li>
<li>Full payment tracking, per program, per time period, or per student</li>
<li>Automated attendance scanning with personalized attendance cards (requires an inexpensive barcode scanner)</li>
<li>Ability to customize the system to set up your own terminology, programs, attendance events, custom fields, etc.</li>
</ul>
